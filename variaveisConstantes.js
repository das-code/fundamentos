var a = 3
let b = 4

var a = 30  // VAR - permite que uma nova variavel com mesmo nome seja declarada
b = 40  // LET - nao permite redeclarar uma varivel de mesmo nome 

console.log(a, b)

// Ambas as formas permitem que um novo valor seja atribuido a uma variavel ja declarada anteriormente
a = 300
b = 400

console.log(a, b)

const c = 5  // CONST - nao permite uma vez aribuido, que seu valor seja mudado

console.log(c)
